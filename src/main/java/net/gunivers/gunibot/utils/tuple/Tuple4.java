package net.gunivers.gunibot.utils.tuple;

public class Tuple4<A, B, C, D> extends Tuple {
	
	private static final long serialVersionUID = 5L;

	public final A _1;
	public final B _2;
	public final C _3;
	public final D _4;

	public Tuple4(A value1, B value2, C value3, D value4) {
		_1 = value1;
		_2 = value2;
		_3 = value3;
		_4 = value4;
	}

}
